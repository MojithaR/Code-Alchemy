//relational operators in C programming
#include<stdio.h>
//function main begins program execution
int main(void)
{
	printf("condition is true = display  1\t , false = display 0\n ");
	//equality
	printf("%d\n ", 10==7);
	printf("%d\n ", 10 > 7);//greter than
	printf("%d\n ", 10 < 7);//less than
	printf("%d\n ", 10 >= 7);//greter than or equal
	printf("%d\n ", 10 <= 7);//less than or equal 
	printf("%d\n ", 10 !=7);//not equal
	
	return 0 ;
}//end of main function
