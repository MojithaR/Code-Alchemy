/*Using Conersion specifiers in a C program*/
#include<stdio.h>
//function main begins program execution

int main(void)
{
	printf("%d\n",  455 );
	printf("%d\n", -455 );
	printf("%i\n",  455 );
	printf("%i\n", -455 );
	printf("%f\n", 1234.56789);
	printf("%.2f\n", 1234.56789);
	printf("%.1f\n", 1234.56789);
	printf("%c\n", 'a');
	
	
	return 0;
}//end of main function
