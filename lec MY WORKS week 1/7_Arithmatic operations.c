//arithmatic operations in c language
#include <stdio.h>
//function main begins program execution
int main (void)
{
	printf(" Addition 1 + 2 = %-d \n", 1+2 );
	printf(" substraction 1 - 2 = %-d \n", 1-2 );
	printf(" multipication  2 * 2 = %-d \n", 2*2 );
	printf(" multipication 2 * 2 = %-f\n", (float)2*2 );
	printf(" multipication 2 * 2 = %-f\n", 2.0*2 );
	printf(" division  9 / 2 = %-f\n", (float)9/2 );
	printf(" division  9 / 2 = %-d\n", 9/2 );
	printf(" division  9 / 2 = %-f\n", 9.0/2 );
	printf(" reminder of  9 / 2 = %-d\n", 9 % 2 );
	
	
	
	return 0;
	
}//end of main function
